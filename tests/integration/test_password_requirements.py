@pytest.fixture
def temp_config(tmp_path):
    """Create a temporary config file with specified encryption settings."""
    def _create_config(encryption_enabled=False, encrypt_emails=False):
        config_path = tmp_path / "config.toml"
        config_data = {
            "general": {
                "encryption_enabled": encryption_enabled,
                "encrypt_emails": encrypt_emails,
                "default_account": "test"
            },
            "accounts": [
                {
                    "friendly_name": "test",
                    "email": "test@example.com",
                    "imap_server": "imap.example.com",
                    "imap_port": 993,
                    "smtp_server": "smtp.example.com",
                    "smtp_port": 465,
                    "username": "test@example.com",
                    "password": "encrypted_or_not"
                }
            ]
        }
        # If encryption is enabled, we need a salt and a canary for verification
        if encryption_enabled or encrypt_emails:
            # Simple dummy salt and canary for "password"
            # In a real scenario, this would be generated by wugong init
            from crypto_utils import encrypt_data
            salt_raw = b"wugong-test-salt"
            config_data["general"]["salt"] = base64.b64encode(salt_raw).decode()
            config_data["general"]["canary"] = encrypt_data("wugong", "password", salt_raw)
            
        with open(config_path, "w") as f:
            toml.dump(config_data, f)
        return config_path
    return _create_config

def test_list_local_no_encryption(temp_config):
    """Scenario 1: No encryption enabled. list --local should not require password."""
    config_path = temp_config(encryption_enabled=False, encrypt_emails=False)
    output = run_wugong_no_password(["list", "--local"], config_path)
    
    # Should return an empty list (no emails) instead of error
    try:
        res = json.loads(output)
        assert isinstance(res, list)
    except json.JSONDecodeError:
        pytest.fail(f"Failed to decode JSON output: {output}")

