def test_init_no_encryption_no_password(tmp_path):
    """Test init with no encryption flags and no password succeeds."""
    config_path = tmp_path / "config.toml"
    output = run_wugong_init(["--no-encrypt-creds", "--no-encrypt-emails"], config_path)
    
    try:
        res = json.loads(output)
        assert res.get("status") == "success"
        
        # Verify config file
        with open(config_path, "r") as f:
            cfg = toml.load(f)
            assert cfg["general"]["encryption_enabled"] is False
            assert cfg["general"]["encrypt_emails"] is False
            assert cfg["general"]["salt"] == ""
    except json.JSONDecodeError:
        pytest.fail(f"Failed to decode JSON output: {output}")

def test_init_with_password_succeeds(tmp_path):
    """Test init with password and encryption flags succeeds."""
    config_path = tmp_path / "config.toml"
    password = "testpassword"
    output = run_wugong_init(["--encrypt-creds", "--encrypt-emails", "-p", password], config_path)
    
    try:
        res = json.loads(output)
        assert res.get("status") == "success"
        
        # Verify config file
        with open(config_path, "r") as f:
            cfg = toml.load(f)
            assert cfg["general"]["encryption_enabled"] is True
            assert cfg["general"]["encrypt_emails"] is True
            assert cfg["general"]["salt"] != ""
            assert "canary" in cfg["general"]
    except json.JSONDecodeError:
        pytest.fail(f"Failed to decode JSON output: {output}")

