import pytest
import os
import io
import json
import toml
import base64
from pathlib import Path
from unittest.mock import patch, MagicMock
from main import main
from logger import disable_logging, enable_logging

def run_wugong_no_password(args, config_path):
    """Run wugong command without providing -p flag."""
    full_args = ["main.py"] + args + ["--non-interactive", "--json"]
    
    disable_logging()
    # We mock get_imap_connection and send_email globally to avoid network issues
    with patch("sys.argv", full_args), \
         patch.dict(os.environ, {"WUGONG_CONFIG": str(config_path)}), \
         patch("mail.connector.MailConnector.get_imap_connection", return_value=MagicMock()), \
         patch("mail.sender.MailSender.send_email", return_value=True), \
         patch("sys.stdout", new=io.StringIO()) as mock_stdout, \
         patch("sys.stderr", new=io.StringIO()) as mock_stderr:
        try:
            main()
        except SystemExit as e:
            # We expect failure in some cases, so we don't raise here
            pass
        raw_output = mock_stdout.getvalue()
    enable_logging()
    return raw_output

