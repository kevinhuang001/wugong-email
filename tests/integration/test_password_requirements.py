def test_send_no_encryption(temp_config):
    """Scenario 5: No encryption enabled. send should not require password."""
    config_path = temp_config(encryption_enabled=False, encrypt_emails=False)
    output = run_wugong_no_password(["send", "--to", "a@b.com", "--subject", "hi", "--body", "test"], config_path)
